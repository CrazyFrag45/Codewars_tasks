def validSolution2(board):
    blocks = [[board[x+a][y+b] for a in (0, 1, 2) for b in (0, 1, 2)] for x in (0, 3, 6) for y in (0, 3, 6)]
    return all(set(x) == set(range(1, 10)) for x in board + list(zip(*board)) + blocks)


